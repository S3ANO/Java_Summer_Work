class video25_class1{
    public static void main (String args[]) {
        System.out.println(Math.abs(-26.7)); //for absolute value
        System.out.println(Math.ceil(7.4)); //rounds up
        System.out.println(Math.floor(7.8)); //rounds down
        System.out.println(Math.max(8.6, 5.2)); //finds the maximum number
        System.out.println(Math.min(8.6, 5.2)); //finds the minimum number
        System.out.println(Math.pow(5, 3)); //power
        System.out.println(Math.sqrt(9)); //square root
    }
}